#include "libft.h"

int		ft_wordcount(char const *s, char c)
{
	int i;
	int f;

	i = 0;
	f = 0;
	while (s[i] != '\0')
	{
		if (s[i] != c && s[i + 1] == c)
			f++;
		i++;
	}
	return (f);
}
