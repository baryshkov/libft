#include "libft.h"

char		*ft_strcat(char *s1, const char *s2)
{
	int l;
	int j;

	j = 0;
	l = ft_strlen(s1);
	while (s2[j] != '\0')
	{
		s1[l++] = s2[j++];
	}
	s1[l] = '\0';
	return (s1);
}
